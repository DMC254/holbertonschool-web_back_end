#!/usr/bin/env python3
"""
This module provides a function to add two numbers.

Example:
    >>> add(1.5, 2.5)
    4.0
"""


def add(a: float, b: float) -> float:
    """
    Adds two numbers and returns the result.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
